function update() {
    var list = $("#comic__modern-list");
    list.empty();
    $.each(Object.keys(comics), function() {
        var publisher = comics[this].publisher.toLowerCase();
        var issue = comics[this].issue.toLowerCase();
        var name = comics[this].series.toLowerCase();
        var currentObject = comics[this];
        var type = comics[this].type.toLowerCase()
        if (age == '') {
            if (pubfilt == '') {
                if (search == '') {
                    template(currentObject)
                } else if (search.toLowerCase().includes(publisher) 
                || search.toLowerCase().includes(issue) 
                || name.startsWith(search)) {
                   template(currentObject)
                }
                    
            } else if (pubfilt) {
                if (publisher == pubfilt) {
                    if (search == "") {
                        template(currentObject)
                    } else if (search.toLowerCase().includes(publisher) && publisher == pubfilt
                    || search.toLowerCase().includes(issue) && publisher == pubfilt
                    || name.startsWith(search) && publisher == pubfilt) {
                       template(currentObject)
            
                    } 
                    
                }
            }  
        } else if (age) {
            if (type == age) {
                if (pubfilt == "") {
                    if (search == "") {
                        template(currentObject)
                } else if (search.toLowerCase().includes(publisher) && publisher == pubfilt
                || search.toLowerCase().includes(issue) && publisher == pubfilt
                || name.startsWith(search) && publisher == pubfilt) {
                   template(currentObject)
        
                }
                } else if (pubfilt) {
                    if (publisher == pubfilt) {
                        if (search == "") {
                            template(currentObject)
                        } else if (search.toLowerCase().includes(publisher) && publisher == pubfilt
                        || search.toLowerCase().includes(issue) && publisher == pubfilt
                        || name.startsWith(search) && publisher == pubfilt) {
                           template(currentObject)
                
                        }
                        
                    }
                }
                
        } 
        
    }
        
    })
    if (list.children().length <= 0) {
        none()
    }
}

